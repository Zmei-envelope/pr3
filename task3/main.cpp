#include <iostream>

int main() {
    // Ввод размера массива
    int n;
    std::cout << "Введите размер массива: ";
    std::cin >> n;

    // Выделение динамической памяти для массива
    int *a = new int[n];

    // Ввод элементов массива
    std::cout << "Введите элементы массива: ";
    for (int i = 0; i < n; ++i) {
        std::cin >> a[i];
    }

    // Поиск отрицательных элементов и вычисление их среднего арифметического
    int count = 0; // Счетчик отрицательных элементов
    double sum = 0; // Сумма отрицательных элементов

    for (int i = 0; i < n; ++i) {
        if (a[i] < 0) { // Если элемент отрицательный
            std::cout << "Отрицательный элемент найден на позиции: " << i << std::endl;
            sum += a[i]; // Добавляем его к сумме
            count++;     // Увеличиваем счетчик
        }
    }

    // Вывод среднего арифметического отрицательных элементов
    if (count > 0) {
        std::cout << "Среднее арифметическое отрицательных элементов: " << sum / count << std::endl;
    } else {
        std::cout << "Отрицательных элементов нет." << std::endl;
    }

    // Освобождение выделенной памяти
    delete[] a;
    return 0;
}
