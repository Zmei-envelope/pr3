#include <stdio.h>
#include <stdlib.h> // Для использования malloc и free

int main() {
    int n; // Размер массива
    printf("Введите размер массива: ");
    scanf("%d", &n);

    // Выделение динамической памяти для массива
    int *a = (int *)malloc(n * sizeof(int));
    if (a == NULL) {
        printf("Ошибка выделения памяти!\n");
        return 1; // Завершаем программу с ошибкой
    }

    // Ввод элементов массива
    printf("Введите элементы массива: ");
    for (int i = 0; i < n; ++i) {
        scanf("%d", &a[i]);
    }

    // Поиск отрицательных элементов и вычисление их среднего арифметического
    int count = 0; // Счетчик отрицательных элементов
    double sum = 0; // Сумма отрицательных элементов

    for (int i = 0; i < n; ++i) {
        if (a[i] < 0) { // Если элемент отрицательный
            printf("Отрицательный элемент найден на позиции: %d\n", i);
            sum += a[i]; // Добавляем его к сумме
            count++;     // Увеличиваем счетчик
        }
    }

    // Вывод среднего арифметического отрицательных элементов
    if (count > 0) {
        printf("Среднее арифметическое отрицательных элементов: %.2f\n", sum / count);
    } else {
        printf("Отрицательных элементов нет.\n");
    }

    // Освобождение выделенной памяти
    free(a);
    return 0;
}
