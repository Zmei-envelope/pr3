#include <stdio.h>
#include <stdlib.h> // Для использования malloc и free

int main() {
    // Выделение динамической памяти для трех указателей на вещественный тип
    float *ptr1 = (float *)malloc(sizeof(float)); // Выделяем память для первого указателя
    float *ptr2 = (float *)malloc(sizeof(float)); // Выделяем память для второго указателя
    float *ptr3 = (float *)malloc(sizeof(float)); // Выделяем память для третьего указателя

    // Проверка, что память успешно выделена
    if (ptr1 == NULL || ptr2 == NULL || ptr3 == NULL) {
        printf("Ошибка выделения памяти!\n");
        return 1; // Завершаем программу с ошибкой
    }

    // Присваивание произвольных значений в выделенные ячейки памяти
    *ptr1 = 10.5; // Присваиваем значение 10.5
    *ptr2 = 20.5; // Присваиваем значение 20.5
    *ptr3 = 30.5; // Присваиваем значение 30.5

    // Вывод исходных значений
    printf("Исходные значения: %.2f, %.2f, %.2f\n", *ptr1, *ptr2, *ptr3);

    // Меняем местами значения первых двух переменных
    float temp = *ptr1; // Сохраняем значение ptr1 во временной переменной
    *ptr1 = *ptr2;      // Присваиваем ptr1 значение ptr2
    *ptr2 = temp;       // Присваиваем ptr2 сохраненное значение

    // Вывод значений после обмена
    printf("После обмена: %.2f, %.2f, %.2f\n", *ptr1, *ptr2, *ptr3);

    // Освобождение выделенной памяти
    free(ptr1);
    free(ptr2);
    free(ptr3);

    return 0;
}
